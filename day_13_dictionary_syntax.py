"""
Definition:
A dictionary is a collection of key-value oairs.
It stores data like a real-world dictionary:
    Word (key) -> Meaning (value)


KEY PROPERTIES:
1. Keys must be uniq"ue.
2. Keys must be immutable types (str, int, tuple).
3. Values can be anything (numbers, lists, dicts, etc).
4. Dictionaries are unordered

"""

# Basic dictionary
student = {
    "name": "Nikhil",
    "age": 26,
    "course": "Data Science"
}


print("Basic dictionary:", student)



# How to create a dictionary

# Method 1: Curly  braces

d1 = {'a': 1, "b": 2}


# Method 2: dict() constructor

d2 = dict(name="FlyinMonk", country="Nepal")

# Method 3: From list of tuples

d3 = dict([("x", 10), ("y", 20)])


print(d1)
print(d2)
print(d3)



# Accessing values

print(student["name"])    # direct acess
print(student.get("age")) # safe access; returns None if missing

# Example of get() wtih default
print(student.get("height", "Not specified"))
print(student.get("length"))



# Adding and updating

student["height"] = 180   # adding
student["age"] = 27       # updating
student.update({"grade": "A"})    # update method


print(student)




# Deleting items

# pop(key) -> removes and returns value
removed = student.pop("grade")
print("Removed:", removed)


# del -> only removes, no return
del student["height"]

# popitem() -> removes last inserted pair
last = student.popitem()

print("removed last item:", last)
print(student)



# Looping through a dictionary

book = {
    "title": "Naked Statistics",
    "author": "Charles Wheelan",
    "year": 2013
}


print("\nKeys:")
for key in book.keys():
  print(key)

print("\nValues:")
for value in book.values():
  print(value)

print("\nKey and value pair:")
for key, value in book.items():
  print(key, "->", value)



# Checking existence

print("Is 'title' a key?", "title" in book )
print("Is 'nikhil' a value?", "Nikhil" in book.values())

# Dictionary methods (MOST IMPORTANT)



# Dictionary comprehensions

# Powerful, compact way to build dictionaries

# squares
squares = {x: x *x for x in range(1,6)}
print("Squares:", squares)


# conditional dictionary
even = {x: x for x in range(10) if x % 2 == 0}
print("Even dict: ", even)

# Merging dictionaries

d1 = {"a": 1, "b":2}
d2 = {"b": 999, "c": 10}


# Method 1: Unpack
merged = {**d1, **d2}
print("Merged (unpacking):", merged)

# Method 2: union operator
merged2 = d1 | d2
print("Merged (| operator):", merged2)

# Copying dictionaries

# shallow copy vs deep copy

import copy

original = {"name": "Flyin", "marks": [1,2,3]}
shallow = original.copy()
deep = copy.deepcopy(original)

original["marks"].append(4)

print("Original:", original)
print("Shallow copy:", shallow)   # affected because list is shared
print("Deep Copy:", deep)         # not affected